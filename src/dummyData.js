function random(seedKey, seedIndex) {
  return seedrandom(`${seedKey}_${seedIndex}`)();
}

function randomData(seedKey, seedIndex, data, excludeData = []) {
  const filteredData = data.filter((item) => !excludeData.includes(item));
  return filteredData[
    Math.floor(random(seedKey, seedIndex) * filteredData.length)
  ];
}

function randRange(seedKey, seedIndex, min, max) {
  return min + Math.floor(random(seedKey, seedIndex)) * (max - min);
}

export function generateRandomData(
  emails = dummyEmails,
  seedKey = 'abc',
  count = 100,
) {
  let seedIndex = 0;
  const data = [];
  for (let i = 0; i < count; i++) {
    const referrer = randomData(seedKey, ++seedIndex, emails);
    const referral1 = randomData(seedKey, ++seedIndex, emails, [referrer]);
    const referral2 = randomData(seedKey, ++seedIndex, emails, [
      referrer,
      referral1,
    ]);
    const sentAt = moment('2000-01-01').add(
      randRange(seedKey, ++seedIndex, 0, 365 * 18),
      'days',
    );
    data.push({
      referrer,
      referral1,
      referral2,
      sentAt,
    });
  }
  return data;
}
